// Conditional Ternary Operator
// syntax 

// condition? statement1 : statement2
/*condition true print statement 1
 condition false print statement2*/

// Example 1

let a: number = 10;
let b: boolean;
//         ifblock: elseblock
b = a > 20 ? true : false;

console.log(a);

console.log(b);

// Example 2

let result: boolean = true;

let finalResult = result ? "onsite" : "online";

console.log(`Congratulations! Now You Are ${finalResult} Student in GIAIC`);
